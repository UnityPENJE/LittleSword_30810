namespace LittleSword.Enemy.FSM
{
    // ============================================================
    // IState: FSM(유한 상태 기계)의 각 상태가 구현해야 할 인터페이스
    // ============================================================
    // FSM(Finite State Machine, 유한 상태 기계)이란?
    //   캐릭터가 가질 수 있는 "상태"를 명확히 정의하고,
    //   조건에 따라 상태 간에 전환되는 시스템이야.
    //
    //   예시: 적의 상태
    //   [Idle: 대기] → 플레이어 감지 → [Chase: 추격] → 공격 범위 진입 → [Attack: 공격]
    //                                   ↑ 놓침 ←──────────────────────────────┘
    //
    // 모든 상태(Idle, Chase, Attack, Die)는 이 인터페이스를 구현해서
    // Enter, Update, Exit 3개의 함수를 반드시 가져야 해.
    // ============================================================
    public interface IState
    {
        // 이 상태로 전환될 때 딱 한 번 호출
        // 상태 진입 시 초기화 작업 (예: 애니메이션 시작, 타이머 초기화)
        void Enter(Enemy enemy);

        // 이 상태 동안 매 프레임 호출 (StateMachine.Update에서 호출)
        // 상태 유지 중 지속적으로 처리할 로직 (예: 이동, 탐지)
        void Update(Enemy enemy);

        // 다른 상태로 전환되기 직전에 딱 한 번 호출
        // 상태 종료 시 정리 작업 (예: 애니메이션 끄기)
        void Exit(Enemy enemy);
    }
}
